void ofApp::drawPointCloud() {
	
    int w = 640;
	int h = 480;
	ofMesh mesh;
	mesh.setMode(OF_PRIMITIVE_POINTS);
	//mesh.setMode(OF_PRIMITIVE_TRIANGLES);
    //mesh.setMode( OF_PRIMITIVE_TRIANGLE_STRIP );
    
	int step = 2;
    
    float mind = cloudThresholdMin;
    float maxd = cloudThresholdMax;
    
    //cout << "vol = " << smoothedVol << "\n";
    float distortion = ofMap( scaledVol, soundThreshold, 1.0, 0.0f, distortionMax, true); 
    
	for(int y = 0; y < h; y += step) {
		for(int x = 0; x < w; x += step) {
			
            float dist = kinect.getDistanceAt(x, y) ; 
            if( dist < maxd && dist > mind ) {
                
                ofVec3f point = kinect.getWorldCoordinateAt(x, y);
                point.x += ofRandom(-distortion, distortion);
                point.y += ofRandom(-distortion, distortion);
                point.z += ofRandom(-distortion, distortion);
                
                float shade = ofMap(dist, maxd , mind, 0.f, 255.f, true);
				mesh.addColor( ofColor( shade, shade*0.5f, shade*0.5f ) );
				mesh.addVertex(point);
			}
		}
	}
	glPointSize(1);
	ofPushMatrix();
	// the projected points are 'upside down' and 'backwards' 
	ofScale(1, -1, -1);
	ofTranslate(0, 0, -1000); // center the points a bit
	ofEnableDepthTest();
	//mesh.drawVertices();
	mesh.drawWireframe();
	ofDisableDepthTest();
	ofPopMatrix();
}

